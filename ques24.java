// Bit manipulation refers to the process of directly handling individual bits of data, using bitwise operators. In computer science, bits (binary digits) are the fundamental units of data, and bit manipulation enables us to perform operations at the bit-level, which can lead to very efficient algorithms for solving certain problems.

// ðŸ”§ Bitwise Operators in Programming:
// These operators allow us to manipulate individual bits of an integer:

// AND (&): Returns 1 if both bits are 1, otherwise returns 0.

// Example: 1010 & 1001 = 1000

// OR (|): Returns 1 if at least one bit is 1.

// Example: 1010 | 1001 = 1011

// XOR (^): Returns 1 if the bits are different, otherwise 0.

// Example: 1010 ^ 1001 = 0011

// NOT (~): Inverts the bits (flips 1 to 0 and vice versa).

// Example: ~1010 = 0101 (flipping all bits).

// Left Shift (<<): Shifts bits to the left, filling with 0 on the right.

// Example: 1010 << 1 = 10100 (multiplies by 2).

// Right Shift (>>): Shifts bits to the right, discarding the bits on the right.

// Example: 1010 >> 1 = 0101 (divides by 2).

