// What is Dynamic Programming (DP)?
// Dynamic Programming is a technique used in algorithm design to solve problems by breaking them down into simpler subproblems, solving each of those only once, and storing their solutions — usually in a table (array, matrix, or hashmap) — so that the same subproblems are not solved again.

// It’s especially useful when a problem has:

// Overlapping Subproblems (same problems are solved multiple times).

// Optimal Substructure (optimal solution can be constructed from optimal solutions of subproblems)